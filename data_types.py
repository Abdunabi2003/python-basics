# Get data type


# Text Type:    str
# Numeric Types:	int, float, complex
# Sequence Types:	list, tuple, range
# Mapping Type:	dict
# Set Types:	set, frozenset
# Boolean Type:	bool
# Binary Types:	bytes, bytearray, memoryview
# None Type:	NoneType

# Example:

#Text type
x="Hello world"
print(x)
print(type(x))

# int
x=20
print(x)
print(type(x))

#  float
x=20.5
print(x)
print(type(x))

# complex
x=1j
print(x)
print(type(x))

# list
x=["apple","banana","cherry"]
print(x)
print(type(x))

# tuple
x=("apple","banana","cherry")
print(x)
print(type(x))

# range
x=range(6)
print(x)
print(type(x))

# dict
x={"name":"John","age":36}
print(x)
print(type(x))

# set 
x={"apple","banana","cherry"}
print(x)
print(type(x))

# bool
x=True
print(x)
print(type(x))

x=None
print(x)
print(type(x))